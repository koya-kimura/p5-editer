let theShader;
let pg;
let w;
let cp;

let buildings = [];

function preload(){
  theShader = loadShader("main.vert", "main.frag");
}

function setup() {
  w = min(windowWidth, windowHeight);
  createCanvas(w, w, WEBGL);

  pg = createGraphics(w, w);

  cp = random(colorPalletes).colors;

  frameRate(10);
}

function draw() {
  background(220);

  pg.background(255);
  if(frameCount%60==10){
    buildings.push(new Building(pg, cp));
  }
  for(let i in buildings){
    buildings[i].display();
    if(buildings[i].p.x < -buildings.sx){
      buildings.splice(i, 1);
    }
  }

  pg.stroke(0);
  pg.strokeWeight(w / 32);
  pg.fill("#432F00");
  pg.rect(0, w*0.8, w, w*0.2);

  shader(theShader);

  theShader.setUniform("u_tex", pg);
  theShader.setUniform("u_time", frameCount / 100);

  rect(0, 0, width, height);
}

class Building {
  constructor(pg){
    this.pg = pg;
    this.p = createVector(w, random(0.1, 0.3)*w);
    this.sx = random(0.3, 0.4)*w;
    this.v = createVector(-6, 0);
    this.c = random(cp);
    this.cc = random(cp);
  }

  display(){
    this.p.add(this.v);

    const h = w - this.p.y;
    this.pg.stroke(0);
    this.pg.strokeWeight(w / 32);
    this.pg.fill(this.c);
    this.pg.rect(this.p.x, this.p.y, this.sx, h);

    this.pg.fill(this.cc);
    this.pg.rect(this.p.x + this.sx * 0.2, this.p.y + h *0.1, this.sx*0.6, h / 4);
  }
}

class Easing {
  static easeInSine(x) {
    return 1 - Math.cos((x * Math.PI) / 2);
  }

  static easeOutSine(x) {
    return Math.sin((x * Math.PI) / 2);
  }

  static easeInOutSine(x) {
    return -(Math.cos(Math.PI * x) - 1) / 2;
  }

  static easeInQuad(x) {
    return x * x;
  }

  static easeOutQuad(x) {
    return 1 - (1 - x) * (1 - x);
  }

  static easeInOutQuad(x) {
    return x < 0.5 ? 2 * x * x : 1 - Math.pow(-2 * x + 2, 2) / 2;
  }

  static easeInCubic(x) {
    return x * x * x;
  }

  static easeOutCubic(x) {
    return 1 - Math.pow(1 - x, 3);
  }

  static easeInOutCubic(x) {
    return x < 0.5 ? 4 * x * x * x : 1 - Math.pow(-2 * x + 2, 3) / 2;
  }

  static easeInQuart(x) {
    return x * x * x * x;
  }

  static easeOutQuart(x) {
    return 1 - Math.pow(1 - x, 4);
  }

  static easeInOutQuart(x) {
    return x < 0.5 ? 8 * x * x * x * x : 1 - Math.pow(-2 * x + 2, 4) / 2;
  }

  static easeInQuint(x) {
    return x * x * x * x * x;
  }

  static easeOutQuint(x) {
    return 1 - Math.pow(1 - x, 5);
  }

  static easeInOutQuint(x) {
    return x < 0.5 ? 16 * x * x * x * x * x : 1 - Math.pow(-2 * x + 2, 5) / 2;
  }

  static easeInExpo(x) {
    return x === 0 ? 0 : Math.pow(2, 10 * x - 10);
  }

  static easeOutExpo(x) {
    return x === 1 ? 1 : 1 - Math.pow(2, -10 * x);
  }

  static easeInOutExpo(x) {
    return x === 0 ?
      0 :
      x === 1 ?
      1 :
      x < 0.5 ?
      Math.pow(2, 20 * x - 10) / 2 :
      (2 - Math.pow(2, -20 * x + 10)) / 2;
  }

  static easeInCirc(x) {
    return 1 - Math.sqrt(1 - Math.pow(x, 2));
  }

  static easeOutCirc(x) {
    return Math.sqrt(1 - Math.pow(x - 1, 2));
  }

  static easeInOutCirc(x) {
    return x < 0.5 ?
      (1 - Math.sqrt(1 - Math.pow(2 * x, 2))) / 2 :
      (Math.sqrt(1 - Math.pow(-2 * x + 2, 2)) + 1) / 2;
  }

  static easeOutBack(x) {
    const c1 = 1.70158;
    const c3 = c1 + 1;
    return 1 + c3 * Math.pow(x - 1, 3) + c1 * Math.pow(x - 1, 2);
  }

  static easeInOutBack(x) {
    const c1 = 1.70158;
    const c2 = c1 * 1.525;
    return x < 0.5 ?
      (Math.pow(2 * x, 2) * ((c2 + 1) * 2 * x - c2)) / 2 :
      (Math.pow(2 * x - 2, 2) * ((c2 + 1) * (x * 2 - 2) + c2) + 2) / 2;
  }
}

